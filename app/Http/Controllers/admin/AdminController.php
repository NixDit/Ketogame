     */
    public function index()
    {
        $data          = (object)[];
        $users         = User::GetAllUsers();
        $data->players = collect();
        // Filter by only players('GUEST')
        if(count($users) > 0){
            foreach ($users as $user) {
                if($user->hasRole('guest')){
                    $data->players[] = $user;
                }
            }    
        } else {
            $data->players = collect();
        }
        // End filter
        $data->tournaments = Tournament::GetAllTournaments();
        $data->players     = collect($data->players);
        return view('admin.pages.dashboard',compact('data'));
    }

